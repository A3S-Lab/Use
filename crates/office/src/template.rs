use std::collections::BTreeMap;
use std::sync::Arc;

use crate::DocumentKind;

pub(crate) fn blank_parts(kind: DocumentKind) -> BTreeMap<String, Arc<[u8]>> {
    let entries = match kind {
        DocumentKind::Word => WORD_PARTS,
        DocumentKind::Spreadsheet => SPREADSHEET_PARTS,
        DocumentKind::Presentation => PRESENTATION_PARTS,
    };
    entries
        .iter()
        .map(|(name, bytes)| ((*name).to_string(), Arc::from(*bytes)))
        .collect()
}

